    int nextPerfectSquare(int a) {
        int square = 0;
        int i = 0;
        while (square <= a) {
            square = i * i;
            i++;
        }
        return square;
    }

    int n_upCount(int[] arr, int n) {
        int previousSum = 0;
        int currentSum = 0;
        int count = 0;
        for (int i = 0; i < arr.length; i++) {
            currentSum += arr[i];
            if (currentSum > n && previousSum <= n)
                count++;
            previousSum = currentSum;
        }
        return count;
    }

